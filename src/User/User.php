<?php

namespace User;

use Entity\EntityTrait;

class User
{


    use EntityTrait;

    /**
     * @var
     */
    private $id;

    /**
     * @var
     */
    private $userName;

    /**
     * @return mixed
     */
    public function getUserName()
    {
        return $this->userName;
    }

    /**
     * @param mixed $userName
     */
    public function setUserName($userName)
    {
        $this->userName = $userName;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

}